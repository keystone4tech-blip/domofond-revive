-- ============================================================
-- ИСПРАВЛЕНИЕ ПРЕДСТАВЛЕНИЯ unique_houses
-- Проблема: старая вью использует split_part(..., 3) и теряет корпуса
-- Например: "г. Краснодар, Корнилова (ул), д. 9, корп. 2"
-- В старой вью = "г. Краснодар, Корнилова (ул), д. 9" (корп. 2 ПОТЕРЯН!)
--
-- Решение: используем regexp_replace для удаления только подъездов и квартир
-- Все корпуса, буквы и дроби (д. 31/1, корп. 2, 6А) СОХРАНЯЮТСЯ
-- ============================================================

-- Удаляем старое представление (если существует)
DROP VIEW IF EXISTS unique_houses;

-- Создаём правильное представление:
-- Формат адреса в таблице accounts:
--   "Город, Улица, д. 9, корп. 2, п. 1, кв. 15"
-- Нам нужно получить:
--   "Город, Улица, д. 9, корп. 2"
-- То есть: убрать всё начиная с ", п." (подъезд) или ", кв." (квартира)
CREATE OR REPLACE VIEW unique_houses AS
SELECT DISTINCT
  regexp_replace(
    regexp_replace(
      address,
      ',\s*(?:п(?:одъезд)?\.?\s*\d+).*$',
      '',
      'i'
    ),
    ',\s*(?:кв\.?\s*[\w]+).*$',
    '',
    'i'
  ) AS house_address
FROM accounts
WHERE address IS NOT NULL AND address <> '';

-- Выдаём права на чтение вью
GRANT SELECT ON unique_houses TO anon, authenticated;

